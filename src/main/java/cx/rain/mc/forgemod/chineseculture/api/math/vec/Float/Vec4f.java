package cx.rain.mc.forgemod.chineseculture.api.math.vec.Float;

/**
 * Vec4f = Vector 4 float 4维向量(float)
 * Vec: Vector
 * f : float
 * @author flysong
 */
public class Vec4f {
    public float x;
    public float y;
    public float z;
    public float t;

    public Vec4f(float x, float y, float z, float t){
        this.x=x;
        this.y=y;
        this.z=z;
        this.t=t;
    }
}
