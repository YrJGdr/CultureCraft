package cx.rain.mc.forgemod.chineseculture.api.math.vec.string;

/**
 * Vec4s = Vector 4 String 4维向量(String)
 * Vec: Vector
 * s : String
 * @author flysong
 */
public class Vec4s {
    public String x;
    public String y;
    public String z;
    public String t;

    public Vec4s(String x, String y, String z, String t){
        this.x=x;
        this.y=y;
        this.z=z;
        this.t=t;
    }
}
