package cx.rain.mc.forgemod.chineseculture.api.math.vec.Float;

/**
 * Vec3f = Vector 3 float 3维向量(float)
 * Vec: Vector
 * f : float
 * @author flysong
 */
public class Vec3f {
    public float x;
    public float y;
    public float z;

    public Vec3f(float x, float y, float z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
}
