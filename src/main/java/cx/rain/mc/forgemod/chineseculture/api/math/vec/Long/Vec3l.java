package cx.rain.mc.forgemod.chineseculture.api.math.vec.Long;

/**
 * Vec3l = Vector 3 long 3维向量(long)
 * Vec: Vector
 * l : long
 * @author flysong
 */
public class Vec3l {
    public long x;
    public long y;
    public long z;

    public Vec3l(long x, long y, long z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
}
