package cx.rain.mc.forgemod.chineseculture.api.math;

/**
 * Vec4i = Vector 4 int 4维向量(int)
 * @author flysong
 */
public class Vec4i {
    public int x;
    public int y;
    public int z;
    public int t;

    public Vec4i(int x,int y,int z,int t){
        this.x=x;
        this.y=y;
        this.z=z;
        this.t=t;
    }
}
