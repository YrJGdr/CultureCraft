package cx.rain.mc.forgemod.chineseculture.api.math.vec.Double;

/**
 * Vec4d = Vector 4 double 4维向量(double)
 * Vec: Vector
 * d : double
 * @author flysong
 */
public class Vec4d {
    public double x;
    public double y;
    public double z;
    public double t;

    public Vec4d(double x, double y, double z, double t){
        this.x=x;
        this.y=y;
        this.z=z;
        this.t=t;
    }
}
