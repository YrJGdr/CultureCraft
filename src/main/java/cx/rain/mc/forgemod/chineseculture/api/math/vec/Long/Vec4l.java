package cx.rain.mc.forgemod.chineseculture.api.math.vec.Long;

/**
 * Vec4l = Vector 4 long 4维向量(long)
 * Vec: Vector
 * l : long
 * @author flysong
 */
public class Vec4l {
    public long x;
    public long y;
    public long z;
    public long t;

    public Vec4l(long x, long y, long z, long t){
        this.x=x;
        this.y=y;
        this.z=z;
        this.t=t;
    }
}
