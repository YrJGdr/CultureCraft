/**
 * Vec3s = Vector 3 String 3维向量(String)
 * Vec: Vector
 * s : String
 * @author flysong
 */
public class Vec3s {
    public String x;
    public String y;
    public String z;

    public Vec3s(String x, String y, String z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
}
